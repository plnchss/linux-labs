#!/bin/bash

get_all_info() {
    echo "========================================"
    echo "Текущий рабочий каталог: $(pwd)"
    echo "========================================"
    echo "Текущий запущенный процесс: $$"
    echo "========================================"
    echo "Домашний каталог: $HOME"
    echo "========================================"
    echo -n "Название и версия ОС: "
    grep "PRETTY_NAME" /etc/os-release | cut -d '"' -f 2
    echo "========================================"
    echo "Доступные оболочки в системе:"
    cat /etc/shells
    echo "========================================"
    echo "Текущие пользователи в системе:"
    who
    echo "========================================"
    echo "Количество пользователей: $(who | wc -l)"
    echo "========================================"
    echo "Информация о жестких дисках:"
    lsblk
    echo "========================================"
    echo "Информация о процессоре:"
    grep "model name" /proc/cpuinfo | head -n 1
    echo "========================================"
    echo "Информация о памяти:"
    free -h
    echo "========================================"
    echo "Информация о файловой системе:"
    df -h
    echo "========================================"
    echo "Информация об установленных пакетах:"
    dpkg -l | head -n 25
}

if [ "$1" == "--tofile" ]; then
    if [ -n "$2" ]; then
        get_all_info > "$2"
        echo "Информация сохранена в файл: $2"
        exit 0
    else
        echo "Ошибка: укажите файл"
        exit 1
    fi
fi

while true; do
    clear
    echo "========================================"
    echo "        ЛАБОРАТОРНАЯ РАБОТА №1"
    echo "========================================"
    echo "1. Текущий рабочий каталог"
    echo "2. Текущий запущенный процесс"
    echo "3. Домашний каталог"
    echo "4. Название и версия ОС"
    echo "5. Доступные оболочки"
    echo "6. Текущие пользователи"
    echo "7. Количество пользователей"
    echo "8. Информация о жестких дисках"
    echo "9. Информация о процессоре"
    echo "10. Информация о памяти"
    echo "11. Информация о файловой системе"
    echo "12. Информация об установленных пакетах"
    echo "13. ВЫВЕСТИ ВСЁ (в консоль)"
    echo "14. Выход"
    echo "========================================"
    echo -n "Выберите пункт меню (1-14): "
    read choice

    case $choice in
        1) pwd ;;
        2) echo "PID: $$" ;;
        3) echo $HOME ;;
        4) grep "PRETTY_NAME" /etc/os-release ;;
        5) cat /etc/shells ;;
        6) who ;;
        7) who | wc -l ;;
        8) lsblk ;;
        9) grep "model name" /proc/cpuinfo | head -n 1 ;;
        10) free -h ;;
        11) df -h ;;
        12) dpkg -l | head -n 20 ;;
        13) get_all_info ;;
        14) break ;;
        *) echo "Ошибка выбора" ;;
    esac

    echo ""
    echo "Для продолжения нажмите Enter"
    read
done
